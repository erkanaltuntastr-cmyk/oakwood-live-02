const translations: Record<Lang, Record<TKey, string>> = { en: t_en, tr: t_tr }

export type TKey = keyof typeof t_en


/** Translate a key, optionally replacing {placeholders} */
export function translate(lang: Lang, key: TKey, vars?: Record<string, string | number>): string {
  let str: string = translations[lang]?.[key] ?? translations['en'][key] ?? key
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replaceAll(`{${k}}`, String(v))
    })
  }
  return str
}
